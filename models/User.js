const mongoose = require('mongoose');

//스키마
const userSchema = mongoose.Schema({
    name: {
        type: String,
        maxlength: 50
    },
    email: {
        type: String,
        trim: true,
        unique: 1
    },
    password: {
        type: String,
        minlength: 5
    },
    lastname: {
        type: String,
        maxlength: 50
    },
    role: {
        type: Number,
        default: 0
    },
    image: String,
    token: {
        type: String
    },
    tokenExp: {
        type: Number
    }
})

//모듈
const User = mongoose.model('User', userSchema);

//다른 곳에서도 사용 할 수 있게 모듈 선언
module.exports = {User};